/* 1. if statement
   2. if else statement
   3. if else if statement
*/

//if
var a = 20;
if (a > 10) {
  document.write("value is greater than 10");
}

//if else
if (a%2 == 0) {
  document.write("a is an even number");
} else {
  document.write("a is an odd number");
}

// if else if
if (a == 10) {
  document.write("a is 10");
}
else if (a == 15) {
  document.write("a is 15");
}
else if (a == 20) {
  document.write("a is 20");
}
else {
  document.write("a is not equal to 10, 15 or 20");
}
